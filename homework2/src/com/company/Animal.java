package com.company;

import com.company.proxy.AnimalActionsProxyI;

public abstract class Animal implements AnimalActions, AnimalActionsProxyI {
    String name;
    int age;

    public Animal(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
